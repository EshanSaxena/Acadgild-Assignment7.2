/*JS code to print multiples of 5 using for loop */
var n = 5;
console.log("Using for loop")
for (var i = 1; i <= 10; i++) {
    console.log(n * i);
}
/*JS code to print multiples of 5 using while loop */
var j = 1;
console.log("Using while loop")
while (j <= 10) {
    console.log(n * j);
    j++;
}